			</main>
			<div class="reasoning">
				<p>When a sick person is not capable of helping himself, he may become a drain on the resources of society.  It becomes an interest of society, then, to prevent that person from getting sick.  As such, society should promote healthy eating, healthy and safe activities, and should discourage unhealthy eating, unealthy and unsafe activities.  Drugs can be seen as making someone sick.  And so, in the interest of society, it becomes an interest of society to prevent the use of some types of drugs that will lead to a drain on society's resources.  However, not all sick people drain society's resources, and not all drugs make people sick.  To the contrary, many currently legal drugs are seen to improve the quality of life of those who use them.</p>
				<p>Unless it can be proven a drug poses a threat to society, that drug should not be disallowed for use.  The high level of imprisonment and fines for the use of drugs which have not been shown to hard society results in a diminished quality of life for those affected directly and indirectly.  However, the selling of drugs of a destructive nature is certainly criminal.</p>

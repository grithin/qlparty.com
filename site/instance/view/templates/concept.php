We require the candidates we endorse to sign a <a href="/contract">contract</a> guaranteeing that:
<ul>
	<li>They remain honest during the campaign and while in office</li>
	<li>They promote the <a href="/aims">aims</a> of the Quality of Life Party, and, in effect, promote an increased quality of life</li>
</ul>
We provide a platform for <a href="/activity">organizing</a> to promote both the candidates and the aims of the Quality of Life party.
<p>
By introducing this simple form of accountability in an otherwise unaccountable system, it is hoped we can begin to reform the system into something better and less corrupt.
</p>